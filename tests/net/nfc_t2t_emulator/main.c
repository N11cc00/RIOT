
#include "net/nfc/t2t/t2t_emulator.h"
#include "net/nfcdev.h"
#include "pn532.h"

static uint8_t memory[] =
{
    0x08, 0xAD, 0xBE, 0xEF, /* UID */
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0xE1, 0x10, 0x12, 0x00,
    0x03, 0x09, 0xD1, 0x01,
    0x05, 0x54, 0x02, 0x65,
    0x6E, 0x48, 0x69, 0xFE,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
};

int main(void) {
    pn532_config_t config = {
        .params = {
            .spi = SPI_DEV(0),
            .reset = GPIO_PIN(1, 1),
            .nss = GPIO_PIN(1, 3),
            .irq = GPIO_PIN(1, 2)
        },
        .mode = PN532_SPI
    };

    pn532_t pn532_dev;

    nfcdev_t dev = {
        .dev = &pn532_dev,
        .ops = &pn532_ops,
        .config = &config,
    };

    nfc_t2t_t *t2t = (nfc_t2t_t *) memory;
    nfc_a_nfcid1_t nfcid1 = {
        .len = NFC_A_NFCID1_LEN4,
        .nfcid = {0x08, 0xAD, 0xBE, 0xEF}
    };

    nfc_t2t_emulator_t emulator;
    t2t_emulator_start(&emulator, &dev, t2t, &nfcid1);

    return 0;
}
